import java.util.*;
abstract class ProblemAbstract{

	//reverse to check pallindrome
	public static boolean solve(char[] pal, int size, int n){
		int count=0,a=n;
		if(size>=0){
			for(int i=0;i<a;i++){

				if(size == n);
				if(pal[size] == pal[n]){
					solve(pal,size+1,n-1);		//recursive call
					count=1;						//count is used as integer flag
				}
			}
		}else{
			count =0;
		}

		//returning true or false
		if(count == 1){
			return true;
		}else{
			return false;
		}
	}	
}