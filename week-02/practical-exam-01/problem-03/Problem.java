//class name Problem
class Problem{
	//Main funstion Execution starts from here
	public static void main(String[] args) {
		//Exception Handling
		try{
			//this for loop if for row do its from 1 to 40
			for(int i =1;i<=40;i++){

				//if row is less than 10 then print *
				if(i<10){
					//Columnn loop
					for(int j=1;j<=i;j++){
						System.out.print("*");
					}
					System.out.println();				//for next line
				}else if(i<20){							//if row is greater than 9 and less than 20 then print =
					for(int j=1;j<=i;j++){				//Column loop and print i number of times
						System.out.print("=");
					}
					System.out.println();				

				}else if (i<30){						//if row is greater than 19 and less than 30 then print x
					for(int j=1;j<=i;j++){				//Column loop and print i number of times
						System.out.print("x");
					}
					System.out.println();

				}else if (i<=40){						//if row is greater than 29 then print o
					for(int j=1;j<=i;j++){				//Column loop and print i number of times.
						System.out.print("o");
					}
					System.out.println();
				}
			}
		}catch(Exception e){
			System.out.println(e);		//Printing exception if occurs
		}
	}	
}