class Triangle extends Shape {
	private double area;

	//Basic Constructor
	Triangle(){
		super();
	}

	//Parameterized Constructor
	Triangle(double width, double height){
		super.setWidth(width);
		super.setHeight(height);
	}

	//Mutators
	public void setArea(){
		area = 0.5 * super.getWidth() * super.getHeight();
	}

	//Accessors
	public double getArea(){
		return area;
	}

	
}