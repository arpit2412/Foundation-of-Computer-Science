public class Book{
	private String title;
	private int year;
	private String publish;
	private int pages;

	
	//Basic Constructor
	Book(){
		this.title = "Unknown";
		this.year = 0;
		this.publish = "Unknown";
		this.pages = 0; 
	}


	//Parameterized Constructor
	Book(String title, int year, String publish, int pages){
		setTitle(title);
		setYear(year);
		setPublish(publish);
		setPages(pages);
	}


	//Mutators
	void setTitle(String title){
		this.title = title;
	}

	void setYear(int year){
		this.year = year;
	}

	void setPublish(String publish){
		this.publish = publish;
	}

	void setPages(int pages){
		this.pages = pages;
	}

	//Accessors
	public String getTitle(){
		return this.title;
	}

	public int getYear(){
		return this.year;
	}

	public String getPublish(){
		return this.publish;
	}

	public int getPages(){
		return this.pages;
	}
}