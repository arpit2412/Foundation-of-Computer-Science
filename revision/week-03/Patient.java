class Patient extends Person{
	private String diagnostic, inTime, prevTime;

	public void setDiagnostic(String diagnostic){
		this.diagnostic = diagnostic;
	}
	public String getDiagnostic(){
		return this.diagnostic;
	}
	public void setinTime(String inTime){
		this.inTime = inTime;
	}
	public String getinTime(){
		return this.inTime;
	}
	public void setprevTime(String prevTime){
		this.prevTime = prevTime;
	}
	public String getprevTime(){
		return this.prevTime;
	}

	Patient(){
		diagnostic=null; 
		inTime = null;
		prevTime=null;
	}
	Patient(String fname, String lname, int age, String gender, String citizenship, String diagnostic ,String inTime, String prevTime){
		super(fname, lname,age,gender, citizenship);
		this.diagnostic = diagnostic;
		this.inTime = inTime;
		this.prevTime = prevTime;
	}
	
}