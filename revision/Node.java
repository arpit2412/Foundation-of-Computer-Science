class Node{
	Student info;
	Node next;
	Node(){
		this.next =null;
		info = new Student();
	}
	Node(Student tmpStudent){
		this.info = tmpStudent;
		this.next = null;
	}
	public void setInfo(Student info){
		this.info = info;
	}
	public Student getInfo(){
		return this.info;
	}
	public void setNext(Node next){
		this.next = next;
	}		
	public Node getNext(){
		return this.next;
	}
}