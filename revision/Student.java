class Student{
	private String name;
	private int age;
	private int period;
	Student(){
		name = null;
		age =0;
		period =0;
	}
	Student(String name, int age, int period){
		this.name = name;
		this.age = age;
		this.period = period;
	}

	public void setName(String name){
		this.name = name;
	}
	public String getName(){
		return this.name;
	}

	public void setAge(int age){
		this.age = age;
	}

	public int getAge(){
		return this.age;
	}

	public void setPeriod(int period){
		this.period = period;
	}

	public int getPeriod(){
		return this.period;
	}

	
	
}