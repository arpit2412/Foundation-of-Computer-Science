class bigCheck{

	public void bigCheck(int a, int b){
		//Identifying bigger number
		if(a>b){
			System.out.println(a +" is bigger than " + b);
		}else if(b>a){
			System.out.println(b +" is bigger than " + a);
		}else{
			System.out.println("Both are equal !!");				//If both numbers are equal
		}
	}	
}