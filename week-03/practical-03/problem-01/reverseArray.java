//Class for reversing the array
class reverseArray{

	//Creating array
	int [] arraInt = new int[20];
	String arraString[] = new String[20];
	float arraFloat[] = new float[20];
	
	//Creating variables
	int i,j;

	//Reversing the order of integer
	int [] reverseArray(int [] array, int n){
		for(i=n-1,j=0;i>=0 && j<n;i--,j++){
			arraInt[j] = array[i];
		}
		return (arraInt);							//returning the reversed ordered array
	}

	//Reversing the order of String
	String [] reverseArray(String [] array, int n){
		
		for(i=n-1,j=0;i>=0 && j<n;i--,j++){
			arraString[j] = array[i];
		}
		return (arraString);						//returning the reversed ordered array
	}

	//Reversing the order of Float
	float [] reverseArray(float [] array,int n){
		
		for(i=n-1,j=0;i>=0 && j<n;i--,j++){
			arraFloat[j] = array[i];
		}
		return (arraFloat);							//returning the reversed ordered array
	}
}